t = turtle.Turtle()
t.shape("turtle")
t.color("purple")
screen = turtle.Screen()

t.forward(50)
t.left(90)
t.forward(50)
t.left(90)
t.forward(50)
t.left(90)
t.forward(50)
t.left(90)

t.up()
t.forward(90)
t.down()

t.color("red")
t.forward(50)
t.left(90)
t.forward(50)
t.left(90)
t.forward(50)
t.left(90)
t.forward(50)
t.left(90)
screen.exitonclick()